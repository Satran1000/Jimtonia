public class Creature 
{
	private int max_health;
	private int health;
	private int armour;
	private int attack;
	private int evasion;
	
	public Creature(int max_health, int health, int armour, int attack, int evasion)
	{
		this.max_health = max_health;
		this.health = health;
		this.armour = armour;
		this.attack = attack;
		this.evasion = evasion;
	}
	
}
